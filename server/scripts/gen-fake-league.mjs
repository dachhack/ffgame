// Generate a seed migration for a sanitized TEST league: Console Warriors' real
// rosters + schedule + real scoring (slugs kept), but fake team names and a
// different 2025 league logo per team. Real NFL players are untouched (real names
// + scoring); only the league's "users" (team names + avatars) are fabricated.
//
// Run from server/:  npx tsx scripts/gen-fake-league.mjs > ../supabase/migrations/0030_fake_test_league.sql
import { buildPlayerIndex } from '../src/playerIndex.js';
import * as sleeper from '../src/sleeper.js';

const SRC = '1313930509658652672';          // Console Warriors (roster/schedule source)
const FAKE_SLEEPER_ID = 'DRIPTEST-2026';    // synthetic id for the fake league
const SEASON = '2026';

// 12 fake teams, each a themed name + the matching 2025 league logo (avatar hash).
const TEAMS = [
  ['Taco Time Titans',      '9ae315a03fbf83b23065f1d98e405388'],
  ['Cheeseburger Chargers', 'f90b463e2c283c2215a3fd26f6780b7e'],
  ['Beach Day Ballers',     'b43cbd8b189f309a4299a862b914766b'],
  ['Cookout Crew',          '7a7b3ea2741db8850467f4daee51c29b'],
  ["Surf's Up Sharks",      '46aafc267ae278acf0cc1c366ded78ae'],
  ['Poolside Punters',      'e4a1d6c5eb9b041f79fa802a037aa4ca'],
  ['Ballpark Blitz',        '22525678eac3ef2f9ae65fc6dd5671c3'],
  ['Road Trip Raiders',     'ccbb3f6090d4c7deb0d6b8f63a67ded6'],
  ['Gone Fishing Phins',    'a11f2ed658865776c8087287eaf17d51'],
  ['Chicken Nuggies',       '0da38888462c687a9b64d04079344b2f'],
  ['Smash Mouth Maulers',   '580df1d9bf71a3e817564ce0d4adbdeb'],
  ['Dunder Mifflin Dynamos','5fc9f197eafa919d157000d0c7e0eb36'],
];
const AVATAR = (h) => `https://sleepercdn.com/avatars/${h}`;
const q = (s) => `'${String(s).replace(/'/g, "''")}'`;

const log = (...a) => console.error(...a);   // diagnostics → stderr (stdout is the SQL)

const idx = await buildPlayerIndex();
const rosters = await sleeper.getLeagueRosters(SRC);
const m1 = await sleeper.getMatchups(SRC, 1);

rosters.sort((a, b) => a.roster_id - b.roster_id);

// Two teams ownable via the normal invite-redeem, so you can log in + play them.
const OWNABLE = { 1: '765446581272072192' }; // roster 1 → dachhack
// roster 2 → huisleon (resolve their id from the source league)
try {
  const u = await sleeper.resolveUser('huisleon');
  if (u?.userId) OWNABLE[2] = u.userId;       // roster 2 → huisleon
} catch { /* leave roster 2 AI if unresolved */ }

// Per-roster starters_json from the REAL roster players, resolved to slug/full/pos
// (superset shape so every consumer is satisfied: player_slug for the resolver,
// slug+full for the client pool).
function lineupFor(roster) {
  const ids = roster.players ?? roster.starters ?? [];
  const out = [];
  let slot = 0;
  for (const sid of ids) {
    const p = idx.sleeper(sid);
    if (!p?.slug || !p.pos) continue;
    if (!['QB', 'RB', 'WR', 'TE', 'K', 'DEF'].includes(p.pos)) continue;
    out.push({ slot: slot++, sleeper_id: String(sid), player_slug: p.slug, slug: p.slug, full: p.full, pos: p.pos });
  }
  return out;
}

const lineups = new Map();
for (const r of rosters) lineups.set(r.roster_id, lineupFor(r));

// week-1 pairings
const pairs = new Map();
for (const r of m1) { if (r.matchup_id != null) { (pairs.get(r.matchup_id) ?? pairs.set(r.matchup_id, []).get(r.matchup_id)).push(r.roster_id); } }

log(`source rosters: ${rosters.length}; pairs: ${[...pairs.values()].filter((p) => p.length === 2).length}; ownable: ${JSON.stringify(OWNABLE)}`);
log('pool sizes:', rosters.map((r) => `${r.roster_id}:${lineups.get(r.roster_id).length}`).join(' '));

// ── Emit SQL ──────────────────────────────────────────────────────────────────
const L = [];
L.push('-- 0030: sanitized TEST league — Console Warriors\' real rosters + schedule +');
L.push('-- real scoring, but fake team names and a different 2025 league logo per team.');
L.push('-- Generated by server/scripts/gen-fake-league.mjs. Idempotent (re-runnable).');
L.push('');
L.push('alter table league_membership add column if not exists avatar_url text;');
L.push('');
L.push('do $$');
L.push('declare dst uuid;');
L.push('begin');
L.push(`  insert into league (sleeper_league_id, season, name, kdst_mode)`);
L.push(`    values (${q(FAKE_SLEEPER_ID)}, ${q(SEASON)}, ${q('Drip Test League')}, 'random')`);
L.push(`  on conflict (sleeper_league_id, season) do update set name = excluded.name, kdst_mode = excluded.kdst_mode`);
L.push('  returning id into dst;');
L.push('');
L.push('  -- clean any prior seed of THIS fake league so re-runs are idempotent');
L.push('  delete from sealed_pick where matchup_id in (select id from matchup where league_id = dst);');
L.push('  delete from matchup_state where matchup_id in (select id from matchup where league_id = dst);');
L.push('  delete from applied_state where matchup_id in (select id from matchup where league_id = dst);');
L.push('  delete from matchup where league_id = dst;');
L.push('  delete from sleeper_lineup where league_id = dst;');
L.push('  delete from league_membership where league_id = dst;');
L.push('');
L.push('  -- teams (fake names + per-team 2025 league logo). Rosters 1-2 are ownable');
L.push('  -- via the invite redeem (real Sleeper ids) so you can log in and play them.');
for (let i = 0; i < rosters.length; i++) {
  const r = rosters[i];
  const [name, avatar] = TEAMS[i % TEAMS.length];
  const owner = OWNABLE[r.roster_id] ?? `FAKE-${r.roster_id}`;
  const controller = OWNABLE[r.roster_id] ? 'human' : 'ai';
  L.push(`  insert into league_membership (league_id, sleeper_roster_id, sleeper_owner_id, app_user_id, enrolled, team_name, controller, avatar_url)`);
  L.push(`    values (dst, ${r.roster_id}, ${q(owner)}, null, false, ${q(name)}, ${q(controller)}, ${q(AVATAR(avatar))});`);
}
L.push('');
L.push('  -- lineups (real rosters → real scoring)');
for (const r of rosters) {
  const json = JSON.stringify(lineups.get(r.roster_id));
  L.push(`  insert into sleeper_lineup (league_id, week, roster_id, starters_json)`);
  L.push(`    values (dst, 1, ${r.roster_id}, ${q(json)}::jsonb);`);
}
L.push('');
L.push('  -- week-1 schedule (mirrors the source pairings), unlocked + editable');
let mid = 0;
for (const [, rs] of pairs) {
  if (rs.length !== 2) continue;
  mid++;
  L.push(`  insert into matchup (league_id, week, sleeper_matchup_id, home_roster_id, away_roster_id, status, lock_at)`);
  L.push(`    values (dst, 1, ${mid}, ${rs[0]}, ${rs[1]}, 'scheduled', now() + interval '7 days');`);
}
L.push('end $$;');
L.push('');
process.stdout.write(L.join('\n'));
